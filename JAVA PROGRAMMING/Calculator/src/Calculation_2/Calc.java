package Calculation_2;

public class Calc {
    public float add(float a, float b) {
        return a + b;
    }

    public float multiply(float a, float b) {
        return a * b;
    }

    public float substract(float a, float b) {
        return a - b;
    }

    public float devide(float a, float b) {
        return a / b;
    }

}
